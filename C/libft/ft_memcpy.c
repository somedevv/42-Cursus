#include "libft.h"
#include <stdio.h>

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	if (dst == NULL || src == NULL)
		return (NULL);
	while (i < n)
	{
		((char *)dst)[i] = ((const char *)src)[i];
		i++;
	}
	return (dst);
}

int main(void)
{
	char *test = NULL;
	char dest[] = "Adios";
	
	printf("%s", ft_memcpy(dest, test, strlen(test)+1));
	return (0);
}
